#!/usr/bin/env python3
"""function floor which takes a float n as argument and
    returns the floor of the float.

    Args:
        n (float): given number from user.

    Returns:
        int: return floor of the float.
    """


def floor(n: float) -> int:
    """function floor which takes a float n as argument and
    returns the floor of the float.

    Args:
        n (float): given number from user.

    Returns:
        int: return floor of the float.
    """
    return int(n)
